@app.route("/chat", methods=["POST"])
def chat():
    """
    Core chat endpoint — sends messages to DigitalOcean Gradient AI.
    Maintains multi-turn history in Flask session.
    Returns reply + extracted code blocks for ZIP download.
    """
    data    = request.get_json()
    message = data.get("message", "").strip()
    mode    = data.get("mode", "chat")

    if not message:
        return jsonify({"error": "Empty message"}), 400

    history = session.get("history", [])

    # Prepend mode context hint so agent knows which feature is active
    mode_hint = MODE_PROMPTS.get(mode, "")
    full_msg  = f"[{mode_hint}]\n\n{message}" if mode_hint else message
    history.append({"role": "user", "content": full_msg})

    # Trim to prevent session overflow
    if len(history) > MAX_HISTORY:
        history = history[-MAX_HISTORY:]

    try:
        reply = call_agent(history)
    except Exception as e:
        return jsonify({"error": f"Agent error: {str(e)}"}), 500

    # Store clean message (without hint) in history
    history[-1]["content"] = message
    history.append({"role": "assistant", "content": reply})
    session["history"] = history[-MAX_HISTORY:]

    return jsonify({"reply": reply, "code_blocks": extract_code_blocks(reply)})


@app.route("/analyze-pdf", methods=["POST"])
def analyze_pdf():
    """
    Accept PDF upload, extract text via pypdf, send to agent for analysis.
    pip install pypdf
    """
    if "file" not in request.files:
        return jsonify({"error": "No file uploaded"}), 400
    if not PYPDF_ENABLED:
        return jsonify({"error": "pypdf not installed. Run: pip install pypdf"}), 500

    pdf_file = request.files["file"]
    question = request.form.get("question", "Analyze this document.")

    try:
        reader   = pypdf.PdfReader(io.BytesIO(pdf_file.read()))
        pdf_text = "\n".join(p.extract_text() or "" for p in reader.pages)
        pdf_text = pdf_text[:6000]  # cap to avoid token overflow
    except Exception as e:
        return jsonify({"error": f"Could not read PDF: {str(e)}"}), 400

    prompt = (
        f"The student has uploaded a PDF document. Extracted content:\n\n---\n"
        f"{pdf_text}\n---\n\nStudent's request: {question}\n\n"
        f"Please analyze the document and respond helpfully."
    )

    try:
        reply = call_agent([{"role": "user", "content": prompt}])
    except Exception as e:
        return jsonify({"error": f"Agent error: {str(e)}"}), 500

    return jsonify({"reply": reply, "code_blocks": extract_code_blocks(reply)})


