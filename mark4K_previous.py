def extract_code_blocks(text):
    """Parse all fenced code blocks from markdown. Returns list of {lang, code}."""
    matches = re.findall(r"```(\w+)?\n(.*?)```", text, re.DOTALL)
    return [{"lang": m[0] or "txt", "code": m[1].strip()} for m in matches]


def build_zip(code_blocks):
    """Package code blocks into an in-memory ZIP. Returns BytesIO."""
    lang_to_file = {
        "python": "app.py", "py": "app.py",
        "html": "templates/index.html",
        "css": "static/style.css",
        "javascript": "static/script.js", "js": "static/script.js",
        "bash": "setup.sh", "sh": "setup.sh",
        "json": "config.json",
    }
    buf = io.BytesIO()
    seen = {}
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        zf.writestr("README.md",
            "# Mark4k Generated Project\n\nGenerated by Mark4k — "
            "DigitalOcean Gradient AI Academic Assistant.\n\n"
            "## Setup\n```\npip install -r requirements.txt\npython app.py\n```\n")
        for i, block in enumerate(code_blocks):
            lang  = block["lang"].lower()
            fname = lang_to_file.get(lang, f"file_{i}.{lang}")
            if fname in seen:
                seen[fname] += 1
                base, ext = os.path.splitext(fname)
                fname = f"{base}_{seen[fname]}{ext}"
            else:
                seen[fname] = 0
            zf.writestr(fname, block["code"])
    buf.seek(0)
    return buf


def generate_pdf(title, content):
    """Render a branded Mark4k PDF document. Returns BytesIO."""
    pdf = FPDF()
    pdf.add_page()
    # Header
    pdf.set_fill_color(9, 17, 31)
    pdf.rect(0, 0, 210, 38, "F")
    pdf.set_text_color(201, 168, 76)
    pdf.set_font("Helvetica", "B", 20)
    pdf.set_xy(12, 9)
    pdf.cell(0, 10, "MARK4K")
    pdf.set_font("Helvetica", "", 9)
    pdf.set_text_color(160, 180, 200)
    pdf.set_xy(12, 22)
    pdf.cell(0, 8, "Academic Assistant  |  Powered by DigitalOcean Gradient AI")
    # Title
    pdf.set_xy(12, 46)
    pdf.set_text_color(20, 20, 20)
    pdf.set_font("Helvetica", "B", 15)
    pdf.multi_cell(186, 9, title)
    # Divider
    pdf.set_draw_color(201, 168, 76)
    pdf.set_line_width(0.6)
    y = pdf.get_y() + 4
    pdf.line(12, y, 198, y)
    pdf.ln(8)
    # Body
    pdf.set_font("Helvetica", "", 11)
    pdf.set_text_color(35, 35, 35)
    clean = re.sub(r"\*\*(.+?)\*\*", r"\1", content)
    clean = re.sub(r"\*(.+?)\*", r"\1", clean)
    clean = re.sub(r"#{1,6}\s+", "", clean)
    clean = re.sub(r"```[\s\S]*?```", "[code block — download ZIP for full code]", clean)
    for line in clean.split("\n"):
        line = line.strip()
        if not line:
            pdf.ln(4)
            continue
        try:
            pdf.multi_cell(186, 7, line)
        except Exception:
            pass
    # Footer
    pdf.set_y(-18)
    pdf.set_font("Helvetica", "I", 8)
    pdf.set_text_color(150, 150, 150)
    pdf.cell(0, 8, "Generated by Mark4k  |  DigitalOcean Gradient AI Hackathon", align="C")
    buf = io.BytesIO()
    pdf.output(buf)
    buf.seek(0)
    return buf


