@app.route("/send-email", methods=["POST"])
def send_email_route():
    """
    Send an academic email via Gmail SMTP.
    Requires a Gmail App Password — NOT the main Gmail password.
    Generate one at: https://myaccount.google.com/apppasswords
    """
    data     = request.get_json()
    to       = data.get("to")
    subject  = data.get("subject")
    body     = data.get("body")
    sender   = data.get("sender")
    password = data.get("password")

    if not all([to, subject, body, sender, password]):
        return jsonify({"error": "All fields are required"}), 400

    try:
        msg = MIMEMultipart()
        msg["From"] = sender; msg["To"] = to; msg["Subject"] = subject
        msg.attach(MIMEText(body, "plain"))
        with smtplib.SMTP_SSL("smtp.gmail.com", 465) as srv:
            srv.login(sender, password)
            srv.sendmail(sender, to, msg.as_string())
        return jsonify({"success": True})
    except smtplib.SMTPAuthenticationError:
        return jsonify({"error": "Gmail auth failed. Use an App Password from myaccount.google.com/apppasswords"}), 400
    except Exception as e:
        return jsonify({"error": str(e)}), 500


